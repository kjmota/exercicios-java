
public class Calculadora {
	
	int soma;
	int subtracao;
	int multiplicacao;
	int divisao;


	protected static int divisao (int numero1, int numero2) {
		return numero1 / numero2;
		
	}

	protected static int multiplicacao (int numero1, int numero2) {
		return numero1 * numero2;
		
	}

	protected static int subtracao (int numero1, int numero2) {
		return numero1 - numero2;
		
	}

	protected static int soma (int numero1, int numero2) {
		return numero1 + numero2;
		
	}

}
